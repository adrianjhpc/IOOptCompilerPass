import subprocess
import re
import argparse
import hashlib
import os

def get_sha256(filepath):
    """Calculates the SHA-256 hash of a file."""
    sha256 = hashlib.sha256()
    try:
        with open(filepath, "rb") as f:
            # Read in 4K chunks to handle large files efficiently
            for block in iter(lambda: f.read(4096), b""):
                sha256.update(block)
        return sha256.hexdigest()
    except Exception as e:
        return None

def count_syscalls(binary_path):
    print(f"--- Running {binary_path} ---")
    cmd = ["strace", "-c", binary_path]
    try:
        result = subprocess.run(cmd, capture_output=True, text=True)
        stats = result.stderr

        write_match = re.search(r'^\s*[\d\.]+\s+[\d\.]+\s+[\d\.]+\s+(\d+).*?\bwrite$', stats, re.MULTILINE)
        writev_match = re.search(r'^\s*[\d\.]+\s+[\d\.]+\s+[\d\.]+\s+(\d+).*?\bwritev$', stats, re.MULTILINE)

        return {
            "write": int(write_match.group(1)) if write_match else 0,
            "writev": int(writev_match.group(1)) if writev_match else 0
        }
    except Exception as e:
        print(f"Error running strace on {binary_path}: {e}")
        return None

def main():
    parser = argparse.ArgumentParser(description="Compare I/O syscalls and verify data integrity.")
    parser.add_argument("standard_exe", help="Path to the unoptimized executable")
    parser.add_argument("fast_exe", help="Path to the optimized executable")
    parser.add_argument("--out-file", dest="out_file", help="The name of the log file generated by the benchmarks to verify (e.g., wal_output.log)", default=None)
    args = parser.parse_args()

    # --- Run Standard Benchmark ---
    if args.out_file and os.path.exists(args.out_file):
        os.remove(args.out_file) # Clean up previous runs
        
    std_data = count_syscalls(args.standard_exe)
    std_hash = None
    
    if args.out_file and os.path.exists(args.out_file):
        os.rename(args.out_file, "standard.out")
        std_hash = get_sha256("standard.out")

    # --- Run Optimized Benchmark ---
    if args.out_file and os.path.exists(args.out_file):
        os.remove(args.out_file)
        
    fast_data = count_syscalls(args.fast_exe)
    fast_hash = None
    
    if args.out_file and os.path.exists(args.out_file):
        os.rename(args.out_file, "fast.out")
        fast_hash = get_sha256("fast.out")

    # --- Print Syscall Results ---
    if std_data is not None and fast_data is not None:
        std_total = std_data['write'] + std_data['writev']
        fast_total = fast_data['write'] + fast_data['writev']

        print("\n" + "="*45)
        print(f"{'Syscall':<15} | {'Standard':<10} | {'Optimized':<10}")
        print("-" * 45)
        print(f"{'write':<15} | {std_data['write']:<10} | {fast_data['write']:<10}")
        print(f"{'writev':<15} | {std_data['writev']:<10} | {fast_data['writev']:<10}")
        print("-" * 45)
        print(f"{'TOTAL I/O':<15} | {std_total:<10} | {fast_total:<10}")
        print("="*45)

        if fast_total < std_total:
            reduction = ((std_total - fast_total) / std_total) * 100
            print(f"SUCCESS: Your pass reduced I/O syscalls by {reduction:.2f}%")
        elif fast_data['writev'] > 0 and std_data['writev'] == 0:
            print("SUCCESS: Converted writes to writev!")
        else:
            print("FAILURE: No I/O optimization detected.")
        print("="*45)

    # --- Print Integrity Results ---
    if args.out_file:
        print("\n" + "="*45)
        print("DATA INTEGRITY CHECK:")
        print(f"Standard Hash : {std_hash or 'FILE NOT FOUND'}")
        print(f"Optimized Hash: {fast_hash or 'FILE NOT FOUND'}")
        print("-" * 45)
        if std_hash and fast_hash and std_hash == fast_hash:
            print("RESULT: EXACT MATCH (Mathematically Sound!)")
        else:
            print("RESULT: MISMATCH! (Data corruption detected)")
        print("="*45)

if __name__ == "__main__":
    main()
