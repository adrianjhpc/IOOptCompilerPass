import subprocess
import argparse
import hashlib
import os
import time

def get_sha256(filepath):
    """Calculates the SHA-256 hash of a file."""
    sha256 = hashlib.sha256()
    try:
        with open(filepath, "rb") as f:
            # Read in 4K chunks to handle large files efficiently
            for block in iter(lambda: f.read(4096), b""):
                sha256.update(block)
        return sha256.hexdigest()
    except Exception as e:
        return None

def time_mpi_execution(binary_path, num_procs):
    """Runs an MPI executable and returns its execution time in seconds."""
    print(f"--- Running {binary_path} on {num_procs} processes ---")
    
    # Standard MPI launch command. Change 'mpiexec' to 'mpirun' if your environment requires it.
    cmd = ["mpiexec", "-n", str(num_procs), binary_path]
    
    start_time = time.perf_counter()
    try:
        subprocess.run(cmd, capture_output=True, text=True, check=True)
        end_time = time.perf_counter()
        return end_time - start_time
    except subprocess.CalledProcessError as e:
        print(f"Error running MPI command for {binary_path}: {e}")
        # Optionally print e.stderr here if you need to debug MPI crashes
        return None
    except FileNotFoundError:
        print(f"Executable or MPI launcher not found. Is MPI installed and in your PATH?")
        return None

def main():
    parser = argparse.ArgumentParser(description="Compare MPI execution times and verify data integrity.")
    parser.add_argument("standard_exe", help="Path to the unoptimized executable")
    parser.add_argument("fast_exe", help="Path to the optimized executable")
    parser.add_argument("-np", "--num-procs", type=int, default=4, help="Number of MPI processes to use (default: 4)")
    parser.add_argument("--out-file", dest="out_file", help="The name of the log file generated by the benchmarks to verify", default=None)
    args = parser.parse_args()

    # --- Run Standard MPI Benchmark ---
    if args.out_file and os.path.exists(args.out_file):
        os.remove(args.out_file)
        
    std_time = time_mpi_execution(args.standard_exe, args.num_procs)
    std_hash = None
    
    if args.out_file and os.path.exists(args.out_file):
        os.rename(args.out_file, "standard.out")
        std_hash = get_sha256("standard.out")

    # --- Run Optimized MPI Benchmark ---
    if args.out_file and os.path.exists(args.out_file):
        os.remove(args.out_file)
        
    fast_time = time_mpi_execution(args.fast_exe, args.num_procs)
    fast_hash = None
    
    if args.out_file and os.path.exists(args.out_file):
        os.rename(args.out_file, "fast.out")
        fast_hash = get_sha256("fast.out")

    # --- Print Timing Results ---
    if std_time is not None and fast_time is not None:
        print("\n" + "="*45)
        print(f"{'Metric':<15} | {'Standard':<10} | {'Optimized':<10}")
        print("-" * 45)
        print(f"{'Time (s)':<15} | {std_time:<10.4f} | {fast_time:<10.4f}")
        print("="*45)

        if fast_time < std_time:
            reduction = ((std_time - fast_time) / std_time) * 100
            speedup = std_time / fast_time
            print(f"SUCCESS: Execution time reduced by {reduction:.2f}%")
            print(f"SPEEDUP: Optimized version is {speedup:.2f}x faster")
        else:
            print("FAILURE: No speedup detected (or optimized version was slower).")
        print("="*45)

    # --- Print Integrity Results ---
    if args.out_file:
        print("\n" + "="*45)
        print("DATA INTEGRITY CHECK:")
        print(f"Standard Hash : {std_hash or 'FILE NOT FOUND'}")
        print(f"Optimized Hash: {fast_hash or 'FILE NOT FOUND'}")
        print("-" * 45)
        if std_hash and fast_hash and std_hash == fast_hash:
            print("RESULT: EXACT MATCH (Mathematically Sound!)")
        else:
            print("RESULT: MISMATCH! (Data corruption detected)")
        print("="*45)

if __name__ == "__main__":
    main()
